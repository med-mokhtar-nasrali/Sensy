// Get all unique games
export const getGamesWithDetailedPros = () => Object.keys(PRO_PLAYERS_DETAILED)

// Get pro players for a specific game
export const getDetailedProsByGame = (game) => PRO_PLAYERS_DETAILED[game] || []

// Get all pro players (flat array)
export const getAllDetailedPros = () => {
  const allPros = []
  Object.entries(PRO_PLAYERS_DETAILED).forEach(([game, players]) => {
    players.forEach(player => {
      allPros.push({ ...player, game })
    })
  })
  return allPros
}

// Search pro players by name, team, or country
export const searchDetailedProPlayers = (query) => {
  const results = []
  const lowerQuery = query.toLowerCase()

  Object.entries(PRO_PLAYERS_DETAILED).forEach(([game, players]) => {
    players.forEach(player => {
      if (player.name.toLowerCase().includes(lowerQuery) ||
        player.team.toLowerCase().includes(lowerQuery) ||
        player.country.toLowerCase().includes(lowerQuery)) {
        results.push({ ...player, game })
      }
    })
  })

  return results
}

// Filter by role
export const filterProsByRole = (role) => {
  const results = []
  Object.entries(PRO_PLAYERS_DETAILED).forEach(([game, players]) => {
    players.forEach(player => {
      if (player.role.toLowerCase() === role.toLowerCase()) {
        results.push({ ...player, game })
      }
    })
  })
  return results
}

// Get unique roles across all games
export const getAllRoles = () => {
  const roles = new Set()
  Object.values(PRO_PLAYERS_DETAILED).forEach(players => {
    players.forEach(player => roles.add(player.role))
  })
  return Array.from(roles).sort()
}

// Get unique teams
export const getAllTeams = () => {
  const teams = new Set()
  Object.values(PRO_PLAYERS_DETAILED).forEach(players => {
    players.forEach(player => teams.add(player.team))
  })
  return Array.from(teams).sort()
}
